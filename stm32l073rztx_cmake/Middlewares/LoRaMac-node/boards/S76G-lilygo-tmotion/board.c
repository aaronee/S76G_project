/*!
 * Initial board STM32L073 and SPI1-SX1276
 *
 *
 *
 */
void BoardInitMcu( void )
{
    if( McuInitialized == false )
    {
        #if defined( USE_BOOTLOADER )
        // Set the Vector Table base location at 0x3000
        SCB->VTOR = FLASH_BASE | 0x3000;
        #endif
        HAL_Init( );
        SystemClockConfig( );
    }
//    else // AARONEE(-)
//    {
//        SystemClockReConfig( );
//    }
    //Enable level shifter for GPS <-> UART4
    GpioInit(&LevelShifter,LevelShifter_OEpin,PIN_OUTPUT,PIN_PUSH_PULL,PIN_PULL_UP,1);
    RtcInit( );

    // Init SPI1 and Init SX1276
    SpiInit( &SX1276.Spi, SPI_2, RADIO_MOSI, RADIO_MISO, RADIO_SCLK, NC );
    SX1276IoInit( );
    if( McuInitialized == false )
    {
        McuInitialized = true;
#ifdef LORA_USE_TCXO

        SX1276SetBoardTcxo(true); // AARONEE(+)
#else
        SX1276SetBoardTcxo(false); // AARONEE(+)
#endif
    }
}

void BoardResetMcu( void )
{
    CRITICAL_SECTION_BEGIN( );

    //Restart system
    NVIC_SystemReset( );
}

void BoardDeInitMcu( void )
{
    // DeInit SPI1 and Init SX1276
    SpiDeInit( &SX1276.Spi );
    SX1276IoDeInit( );
}

uint32_t BoardGetRandomSeed( void )
{
    return ( ( *( uint32_t* )ID1 ) ^ ( *( uint32_t* )ID2 ) ^ ( *( uint32_t* )ID3 ) );
}

void BoardGetUniqueId( uint8_t *id )
{
    id[7] = ( ( *( uint32_t* )ID1 )+ ( *( uint32_t* )ID3 ) ) >> 24;
    id[6] = ( ( *( uint32_t* )ID1 )+ ( *( uint32_t* )ID3 ) ) >> 16;
    id[5] = ( ( *( uint32_t* )ID1 )+ ( *( uint32_t* )ID3 ) ) >> 8;
    id[4] = ( ( *( uint32_t* )ID1 )+ ( *( uint32_t* )ID3 ) );
    id[3] = ( ( *( uint32_t* )ID2 ) ) >> 24;
    id[2] = ( ( *( uint32_t* )ID2 ) ) >> 16;
    id[1] = ( ( *( uint32_t* )ID2 ) ) >> 8;
    id[0] = ( ( *( uint32_t* )ID2 ) );
}

